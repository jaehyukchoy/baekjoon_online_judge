import sys
input=sys.stdin.readline
n=int(input())
a=list(map(int,input().split()))
c0=a.count(0)
c1=a.count(1)
c2=a.count(2)
c3=a.count(3)
ans=0
p3_1=min(c0,c3)
ans+=3*p3_1
c0-=p3_1
c3-=p3_1
p3_2=min(c1,c2)
ans+=3*p3_2
c1-=p3_2
c2-=p3_2
p2_1=min(c0,c2)
ans+=2*p2_1
c0-=p2_1
c2-=p2_1
p2_2=min(c1,c3)
ans+=2*p2_2
c1-=p2_2
c3-=p2_2
p1_1=min(c2,c3)
ans+=p1_1
c2-=p1_1
c3-=p1_1
p1_2=min(c0,c1)
ans+=p1_2
c0-=p1_2
c1-=p1_2
print(ans)